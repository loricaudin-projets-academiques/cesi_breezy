export type TranslationKey = keyof typeof translations.fr;

export function getT(lang: string) {
  return (key: TranslationKey, params?: Record<string, string | number>): string => {
    const dict = translations[lang as 'fr' | 'en'] || translations.fr;
    let text: string = dict[key] || translations.fr[key] || String(key);
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        text = text.replace(`{${k}}`, String(v));
      });
    }
    return text;
  };
}

export function useTranslation() {
  const { profile } = useBreezyApp();
  const lang = profile?.user?.language || 'fr';
  return { t: getT(lang), lang };
}
